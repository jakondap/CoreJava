package com.linkedlist;

public class Bogie {
	private String name;
	private Bogie next;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Bogie getNext() {
		return next;
	}
	public void setNext(Bogie next) {
		this.next = next;
	}
	
	

}
